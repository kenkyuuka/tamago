CONTROL_BLOCK_SIGNATURE = b" Encryption control block"
CONTROL_BLOCK_SIZE = 0x400  # 1024 uint32 values


def extract_control_block(tpm_data):
    """Extract and invert the control block from a TPM plugin binary.

    Returns a list of 1024 uint32 values, or None if the signature is not found.
    """
    idx = tpm_data.find(CONTROL_BLOCK_SIGNATURE)
    if idx < 0:
        return None
    raw = struct.unpack_from(f"<{CONTROL_BLOCK_SIZE}I", tpm_data, idx)
    return [_u32(~v) for v in raw]


