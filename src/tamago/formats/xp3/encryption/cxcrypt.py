# PRNG factories
# ---------------------------------------------------------------------------


def make_lcg_prng(seed):
    """Create a standard LCG PRNG closure for CxProgram."""
    state = [seed]

    def prng():
        old = state[0]
        state[0] = _u32(1103515245 * state[0] + 12345)
        return _u32(state[0] ^ _u32(old << 16) ^ _u32(old >> 16))

    return prng


def make_nana_prng(seed, random_seed):
    """Create a CxProgramNana xorshift PRNG closure for CxProgram.

    Used by CabbageCxCrypt / NanaCxCrypt / RiddleCxCrypt (YuzuSoft-family).
    """
    state = [seed, random_seed]

    def prng():
        s = _u32(state[0] ^ _u32(state[0] << 17))
        s = _u32(s ^ (_u32(s << 18) | (s >> 15)))
        state[0] = _u32(~s)
        r = _u32(state[1] ^ _u32(state[1] << 13))
        r = r ^ (r >> 17)
        state[1] = _u32(r ^ _u32(r << 5))
        return _u32(state[0] ^ state[1])

    return prng


