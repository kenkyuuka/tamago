class CxEncryption(XP3Encryption):
    """CxEncryption-based XP3 decryption.

    The control block can be provided directly or loaded from a TPM file.
    When ``tpm_file`` is given, the control block is extracted automatically
    using :func:`extract_control_block`.

    Args:
        control_block: List of 1024 uint32 values (inverted from TPM).
            Mutually exclusive with ``tpm_file``.
        tpm_file: Path to a TPM plugin file to extract the control block from.
            Mutually exclusive with ``control_block``.
        mask: Threshold mask for the hash-based offset split.
        offset: Threshold offset added to the masked hash.
        prolog_order: 3-element permutation of [0, 1, 2].
        odd_branch_order: 6-element permutation of [0, 1, 2, 3, 4, 5].
        even_branch_order: 8-element permutation of [0, 1, 2, 3, 4, 5, 6, 7].
        prng_type: ``"lcg"`` for standard or ``"nana"`` for CxProgramNana.
        random_seed: Extra seed for the ``"nana"`` PRNG variant.
    """

    def __init__(
        self,
        control_block=None,
        mask=0,
        offset=0,
        prolog_order=None,
        odd_branch_order=None,
        even_branch_order=None,
        prng_type="lcg",
        random_seed=0,
        tpm_file=None,
    ):
        if control_block is None and tpm_file is None:
            raise ValueError("Either control_block or tpm_file must be provided")
        if control_block is None:
            with open(tpm_file, "rb") as f:
                tpm_data = f.read()
            control_block = extract_control_block(tpm_data)
            if control_block is None:
                raise ValueError(f"No control block signature found in {tpm_file}")

        self._control_block = list(control_block)
        self._mask = mask
        self._offset = offset
        self._prolog_order = prolog_order or [0, 1, 2]
        self._odd_branch_order = odd_branch_order or [0, 1, 2, 3, 4, 5]
        self._even_branch_order = even_branch_order or [0, 1, 2, 3, 4, 5, 6, 7]
        self._prng_type = prng_type
        self._random_seed = random_seed

        if prng_type == "nana":
            self._prng_factory = lambda seed: make_nana_prng(seed, random_seed)
        else:
            self._prng_factory = None  # uses default LCG

        self._codegen = CxCodeGenerator(
            self._control_block,
            self._prolog_order,
            self._odd_branch_order,
            self._even_branch_order,
            self._prng_factory,
        )
        self._program_cache = {}

    def _get_program(self, seed):
        if seed not in self._program_cache:
            self._program_cache[seed] = self._codegen.generate(seed)
        return self._program_cache[seed]

    def _execute_xcode(self, hash_val):
        seed = hash_val & 0x7F
        program = self._get_program(seed)
        h = hash_val >> 7
        ret1 = program.execute(_u32(h))
        ret2 = program.execute(_u32(~h))
        return ret1, ret2

    def _decode(self, buf, key, file_offset):
        """Apply CxEncryption XOR to buffer bytes."""
        ret1, ret2 = self._execute_xcode(key)
        key1 = (ret2 >> 16) & 0xFFFF
        key2 = ret2 & 0xFFFF
        key3 = ret1 & 0xFF
        if key1 == key2:
            key2 = _u32(key2 + 1)
        if key3 == 0:
            key3 = 1

        # Bulk XOR with key3 (point XORs at pos1/pos2 commute with it)
        result = bytearray(buf.translate(bytes(i ^ key3 for i in range(256))))

        # Point XOR at specific offsets
        pos2 = key2 - file_offset
        if 0 <= pos2 < len(result):
            result[pos2] ^= (ret1 >> 16) & 0xFF

        pos1 = key1 - file_offset
        if 0 <= pos1 < len(result):
            result[pos1] ^= (ret1 >> 8) & 0xFF

        return bytes(result)

    def _get_base_offset(self, hash_val):
        return _u32((hash_val & self._mask) + self._offset)

    def _apply(self, data, info, segment):
        """Decrypt or encrypt (symmetric XOR).

        The file_offset is always 0 because XP3 segments are decrypted
        independently starting from the beginning of each segment's data.
        The segment.offset field is the archive-level byte position, not
        the file-local offset used by CxEncryption's threshold split.
        """
        hash_val = _u32(info.key)
        base_offset = self._get_base_offset(hash_val)
        file_offset = 0

        buf = bytearray(data)
        pos = 0

        if file_offset < base_offset:
            base_length = min(base_offset - file_offset, len(buf))
            chunk = self._decode(bytes(buf[:base_length]), hash_val, file_offset)
            buf[:base_length] = chunk
            file_offset += base_length
            pos = base_length

        if pos < len(buf):
            transformed_key = _u32((hash_val >> 16) ^ hash_val)
            chunk = self._decode(bytes(buf[pos:]), transformed_key, file_offset)
            buf[pos:] = chunk

        return bytes(buf)

    def decrypt(self, data, info, segment):
        return self._apply(data, info, segment)

    def encrypt(self, data, info, segment):
        return self._apply(data, info, segment)
