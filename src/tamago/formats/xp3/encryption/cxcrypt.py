"""XP3 CxEncryption — bytecode VM-based encryption used by KiriKiri TPM plugins.

This implements the CxEncryption system used by many KiriKiri games (YuzuSoft,
Nanawind, and others). The encryption uses a bytecode virtual machine that
generates per-file XOR programs from a control block extracted from the game's
TPM plugin.

The algorithm:
1. Split the file at a threshold offset: ``(hash & mask) + offset``
2. Before the threshold, derive keys from ``hash``
3. After the threshold, derive keys from ``(hash >> 16) ^ hash``
4. Key derivation runs the bytecode VM twice (with ``hash >> 7`` and ``~(hash >> 7)``)
   and extracts XOR key bytes from the results

Based on GARbro's CxEncryption implementation (KiriKiriCx.cs, YuzCrypt.cs).
"""

from __future__ import annotations

import struct

from .base import XP3Encryption

U32_MASK = 0xFFFFFFFF


def _u32(val):
    """Truncate to unsigned 32-bit."""
    return val & U32_MASK


def _rotl(val, n):
    """32-bit rotate left."""
    val &= U32_MASK
    n &= 31
    return _u32((val << n) | (val >> (32 - n)))


def _rotr(val, n):
    """32-bit rotate right."""
    val &= U32_MASK
    n &= 31
    return _u32((val >> n) | (val << (32 - n)))


# ---------------------------------------------------------------------------
# Bytecode VM
# ---------------------------------------------------------------------------


class CxProgram:
    """Bytecode program generated by CxEncryption for a specific seed.

    Each unique ``hash & 0x7F`` value produces a different program. The program
    is a list of (opcode, operand) tuples interpreted by :meth:`execute`.
    """

    LENGTH_LIMIT = 0x80

    # Opcodes that consume an immediate value from the next code entry.
    _IMMED_OPCODES = frozenset(
        {
            "ADD_EAX_IMM",
            "SUB_EAX_IMM",
            "AND_EBX_IMM",
            "AND_EAX_IMM",
            "XOR_EAX_IMM",
            "MOV_EAX_IMM",
        }
    )

    def __init__(self, seed, control_block, random_fn=None):
        self._seed = seed
        self._control_block = control_block
        self._random_fn = random_fn or self._default_random
        self._code = []  # flat list of opcodes/immediates
        self._length = 0

    # -- PRNG variants -------------------------------------------------------

    def _default_random(self):
        """Standard LCG PRNG (CxProgram)."""
        old = self._seed
        self._seed = _u32(1103515245 * self._seed + 12345)
        return _u32(self._seed ^ _u32(old << 16) ^ _u32(old >> 16))

    def get_random(self):
        return self._random_fn()

    # -- Code emission -------------------------------------------------------

    def clear(self):
        self._code.clear()
        self._length = 0

    def emit_nop(self, count):
        if self._length + count > self.LENGTH_LIMIT:
            return False
        self._length += count
        return True

    def emit(self, opcode, length=1):
        if self._length + length > self.LENGTH_LIMIT:
            return False
        self._length += length
        self._code.append(opcode)
        return True

    def emit_u32(self, value):
        if self._length + 4 > self.LENGTH_LIMIT:
            return False
        self._length += 4
        self._code.append(_u32(value))
        return True

    def emit_random(self):
        return self.emit_u32(self.get_random())

    # -- Execution -----------------------------------------------------------

    def execute(self, hash_val):
        eax = ebx = ecx = edi = _u32(0)
        stack = []
        it = iter(self._code)

        for entry in it:
            # If the entry is an integer, skip (stale immediate — shouldn't happen).
            if isinstance(entry, int):
                continue

            opcode = entry

            # Instructions that consume an immediate: read the next entry first.
            imm = 0
            if opcode in self._IMMED_OPCODES:
                imm = next(it)

            if opcode == "NOP":
                continue
            elif opcode == "MOV_EDI_ARG":
                edi = _u32(hash_val)
            elif opcode == "PUSH_EBX":
                stack.append(ebx)
            elif opcode == "POP_EBX":
                ebx = stack.pop()
            elif opcode == "PUSH_ECX":
                stack.append(ecx)
            elif opcode == "POP_ECX":
                ecx = stack.pop()
            elif opcode == "MOV_EBX_EAX":
                ebx = eax
            elif opcode == "MOV_EAX_EDI":
                eax = edi
            elif opcode == "MOV_ECX_EBX":
                ecx = ebx
            elif opcode == "MOV_EAX_EBX":
                eax = ebx
            elif opcode == "AND_ECX_0F":
                ecx = ecx & 0x0F
            elif opcode == "SHR_EBX_1":
                ebx = ebx >> 1
            elif opcode == "SHL_EAX_1":
                eax = _u32(eax << 1)
            elif opcode == "SHR_EAX_CL":
                eax = _u32(eax >> (ecx & 0x1F))
            elif opcode == "SHL_EAX_CL":
                eax = _u32(eax << (ecx & 0x1F))
            elif opcode == "OR_EAX_EBX":
                eax = eax | ebx
            elif opcode == "NOT_EAX":
                eax = _u32(~eax)
            elif opcode == "NEG_EAX":
                eax = _u32(-eax)
            elif opcode == "DEC_EAX":
                eax = _u32(eax - 1)
            elif opcode == "INC_EAX":
                eax = _u32(eax + 1)
            elif opcode == "ADD_EAX_EBX":
                eax = _u32(eax + ebx)
            elif opcode == "SUB_EAX_EBX":
                eax = _u32(eax - ebx)
            elif opcode == "IMUL_EAX_EBX":
                eax = _u32(eax * ebx)
            elif opcode == "ADD_EAX_IMM":
                eax = _u32(eax + imm)
            elif opcode == "SUB_EAX_IMM":
                eax = _u32(eax - imm)
            elif opcode == "AND_EBX_IMM":
                ebx = ebx & imm
            elif opcode == "AND_EAX_IMM":
                eax = eax & imm
            elif opcode == "XOR_EAX_IMM":
                eax = eax ^ imm
            elif opcode == "MOV_EAX_IMM":
                eax = imm
            elif opcode == "MOV_EAX_INDIRECT":
                if eax >= len(self._control_block):
                    raise RuntimeError(
                        f"CxProgram: index {eax} out of bounds (control block size {len(self._control_block)})"
                    )
                eax = _u32(~self._control_block[eax])
            elif opcode == "RETN":
                if stack:
                    raise RuntimeError("CxProgram: imbalanced stack at RETN")
                return eax
            else:
                raise RuntimeError(f"CxProgram: unknown opcode {opcode!r}")

        raise RuntimeError("CxProgram: no RETN")


