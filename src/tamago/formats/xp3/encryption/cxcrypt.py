# Code generator
# ---------------------------------------------------------------------------


class CxCodeGenerator:
    """Generates CxProgram bytecode using the CxEncryption algorithm."""

    def __init__(self, control_block, prolog_order, odd_branch_order, even_branch_order, prng_factory=None):
        self._control_block = control_block
        self._prolog_order = prolog_order
        self._odd_branch_order = odd_branch_order
        self._even_branch_order = even_branch_order
        self._prng_factory = prng_factory

    def generate(self, seed):
        """Generate a CxProgram for the given seed (hash & 0x7F).

        The program object is created once and reused across stage attempts.
        When a stage fails (program too large), the code is cleared but the
        PRNG state carries forward — matching the TPM's behavior.
        """
        program = CxProgram(seed, self._control_block, self._prng_factory(seed) if self._prng_factory else None)
        for stage in range(5, 0, -1):
            if self._emit_code(program, stage):
                return program
            program.clear()
        raise RuntimeError("CxEncryption: overly large bytecode")

    def _emit_code(self, p, stage):
        return (
            p.emit_nop(5)
            and p.emit("MOV_EDI_ARG", 4)
            and self._emit_body(p, stage)
            and p.emit_nop(5)
            and p.emit("RETN")
        )

    def _emit_body(self, p, stage):
        if stage == 1:
            return self._emit_prolog(p)

        if not p.emit("PUSH_EBX"):
            return False

        if p.get_random() & 1:
            ok = self._emit_body(p, stage - 1)
        else:
            ok = self._emit_body2(p, stage - 1)
        if not ok:
            return False

        if not p.emit("MOV_EBX_EAX", 2):
            return False

        if p.get_random() & 1:
            ok = self._emit_body(p, stage - 1)
        else:
            ok = self._emit_body2(p, stage - 1)
        if not ok:
            return False

        return self._emit_odd_branch(p) and p.emit("POP_EBX")

    def _emit_body2(self, p, stage):
        if stage == 1:
            return self._emit_prolog(p)

        if p.get_random() & 1:
            ok = self._emit_body(p, stage - 1)
        else:
            ok = self._emit_body2(p, stage - 1)
        if not ok:
            return False

        return self._emit_even_branch(p)

    def _emit_prolog(self, p):
        choice = self._prolog_order[p.get_random() % 3]
        if choice == 2:
            return (
                p.emit_nop(5)
                and p.emit("MOV_EAX_IMM", 2)
                and p.emit_u32(p.get_random() & 0x3FF)
                and p.emit("MOV_EAX_INDIRECT", 0)
            )
        elif choice == 1:
            return p.emit("MOV_EAX_EDI", 2)
        elif choice == 0:
            return p.emit("MOV_EAX_IMM") and p.emit_random()
        return False

    def _emit_even_branch(self, p):
        choice = self._even_branch_order[p.get_random() & 7]
        if choice == 0:
            return p.emit("NOT_EAX", 2)
        elif choice == 1:
            return p.emit("DEC_EAX")
        elif choice == 2:
            return p.emit("NEG_EAX", 2)
        elif choice == 3:
            return p.emit("INC_EAX")
        elif choice == 4:
            return p.emit_nop(5) and p.emit("AND_EAX_IMM") and p.emit_u32(0x3FF) and p.emit("MOV_EAX_INDIRECT", 3)
        elif choice == 5:
            return (
                p.emit("PUSH_EBX")
                and p.emit("MOV_EBX_EAX", 2)
                and p.emit("AND_EBX_IMM", 2)
                and p.emit_u32(0xAAAAAAAA)
                and p.emit("AND_EAX_IMM")
                and p.emit_u32(0x55555555)
                and p.emit("SHR_EBX_1", 2)
                and p.emit("SHL_EAX_1", 2)
                and p.emit("OR_EAX_EBX", 2)
                and p.emit("POP_EBX")
            )
        elif choice == 6:
            return p.emit("XOR_EAX_IMM") and p.emit_random()
        elif choice == 7:
            if p.get_random() & 1:
                ok = p.emit("ADD_EAX_IMM")
            else:
                ok = p.emit("SUB_EAX_IMM")
            return ok and p.emit_random()
        return False

    def _emit_odd_branch(self, p):
        choice = self._odd_branch_order[p.get_random() % 6]
        if choice == 0:
            return (
                p.emit("PUSH_ECX")
                and p.emit("MOV_ECX_EBX", 2)
                and p.emit("AND_ECX_0F", 3)
                and p.emit("SHR_EAX_CL", 2)
                and p.emit("POP_ECX")
            )
        elif choice == 1:
            return (
                p.emit("PUSH_ECX")
                and p.emit("MOV_ECX_EBX", 2)
                and p.emit("AND_ECX_0F", 3)
                and p.emit("SHL_EAX_CL", 2)
                and p.emit("POP_ECX")
            )
        elif choice == 2:
            return p.emit("ADD_EAX_EBX", 2)
        elif choice == 3:
            return p.emit("NEG_EAX", 2) and p.emit("ADD_EAX_EBX", 2)
        elif choice == 4:
            return p.emit("IMUL_EAX_EBX", 3)
        elif choice == 5:
            return p.emit("SUB_EAX_EBX", 2)
        return False


